/**
 * Returns a translation function for the given language.
 * @param {string} lang - 'en' | 'fr'
 * @returns {function} t(key) → string
 */
export function getT(lang) {
  const locale = lang === 'fr' ? 'fr' : 'en';
  return (key) => T[locale]?.[key] ?? T['en']?.[key] ?? key;
}
